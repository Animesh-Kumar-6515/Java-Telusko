public class Switch {
    public static void main(String args[]){
        System.out.println("Hi this is Switch tutorial!!");
        int n=2;
        switch (n){
            case 1:
                System.out.println("Sunday");
                break;
            case 2:
                System.out.println("Monday");
                break;
            case 3:
                System.out.println("Tuesday");
                break;
            default:
                System.out.println("No day!!!");
                break;
        }
    }
}
